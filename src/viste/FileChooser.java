private void flChooserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_flChooserActionPerformed
// TODO add your handling code here:
    if (evt.getActionCommand().equals(OK)) {
        
        File file = flChooser.getSelectedFile();
        final int RESPONSE = JOptionPane.showConfirmDialog(null, "Sei sicuro di voler ripristinare la versione selezionata del database?", "Conferma restore", JOptionPane.OK_CANCEL_OPTION);
        if (RESPONSE == JOptionPane.OK_OPTION) {
            try {
                FrontController.test();            
                    try {
                        FrontController.restore(file);
                        dispose();
                        JOptionPane.showMessageDialog(this, "Ripristino del database eseguito con successo!", "", JOptionPane.INFORMATION_MESSAGE);
                        
                    } catch (IOException ex) {
                        Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (EccezioneDumpNonTrovato e) {
                        JOptionPane.showMessageDialog(this, e.getMessage(), "File non trovato", JOptionPane.ERROR_MESSAGE);
                    }

            } catch (EccezioneConnesioneNonRiuscita e) {
                JOptionPane.showMessageDialog(null, "Impossibile collegarsi al database!\n" + e.getMessage(), "Connessione non riuscita", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else   
        dispose();
    
}//GEN-LAST:event_flChooserActionPerformed


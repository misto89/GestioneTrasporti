private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
// TODO add your handling code here:
    txtGiorno.setDocument(new JTextFieldLimit(MAX_LENGTH_GIORNO));
    txtMese.setDocument(new JTextFieldLimit(MAX_LENGTH_MESE));
    txtAnno.setDocument(new JTextFieldLimit(MAX_LENGTH_ANNO));
    txtImporto.setDocument(new JTextFieldFormatDouble());
    
    java.util.Date utilDate = new java.util.Date();    
    String today = (new java.sql.Date(utilDate.getTime())).toString();
    String[] splitted = today.split("\\-");
    String anno = splitted[0];
    String mese = splitted[1];
    String giorno = splitted[2];
    txtGiorno.setText(giorno);
    txtMese.setText(mese);
    txtAnno.setText(anno);
    
}//GEN-LAST:event_formWindowOpened

private void btnRegistraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistraActionPerformed
// TODO add your handling code here:
    String anno = txtAnno.getText();
    String mese = txtMese.getText();
    String giorno = txtGiorno.getText();
    Date data = null;
    
    try {
        data = Date.valueOf(anno + "-" + mese + "-" + giorno);
        
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, "Inserire la data nel formato corretto!", "Errore", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    String banca = txtBanca.getText();
    
    double importo = 0.00;
    try {
        importo = Double.parseDouble(txtImporto.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Inserire un valore decimale per l'importo!", "Errore", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    MovimentazioneContante movimento = null;
    if (type.equalsIgnoreCase(MovimentazioneContante.VERSAMENTO))
        movimento = new Versamento(null, data, banca, importo);
    else if (type.equalsIgnoreCase(MovimentazioneContante.PRELIEVO))
        movimento = new Prelievo(null, data, banca, importo);
    if (FrontController.doCashMovement(movimento)){
        parent.setTable();
        dispose();
    }
}//GEN-LAST:event_btnRegistraActionPerformed

private boolean checkData(String data) {
    Pattern pattern = Pattern.compile("\\d{1,2}/\\d{1,2}/\\d{2,4}");
    Matcher match = pattern.matcher(data);
    return match.matches();
}

